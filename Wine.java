/**
 * wine
 */
public class Wine {
    String id;
    float value;
    int quantity;
    String seller;
    String imgPath;

    public Wine(String id, float value, int quantity, String seller, String imgPath) {
        this.id = id;
        this.value = value;
        this.quantity = quantity;
        this.seller = seller;
        this.imgPath = imgPath;
    }

}